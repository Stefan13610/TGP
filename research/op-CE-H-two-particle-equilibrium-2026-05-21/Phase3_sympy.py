def dphi_AK_K_dx(x_arr, L):
    """Derivative of two-soliton ansatz."""
    k = m_num / np.sqrt(2)
    # d/dx [-v*tanh(k*(x+L/2))] = -v*k*sech^2(k*(x+L/2))
